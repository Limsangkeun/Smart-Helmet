package cc.kocoafab.android.orangeblechat;

/**
 * 채팅 창에 메시지를 표시하기 위한 모델
 */
public class Message {

    public static final int MSG_IN = 0x01;
    public static final int MSG_OUT = 0x02;
    public static final int STATUS_SUCC = 0x01;
    public static final int STATUS_FAIL = 0x02;

    private String from;
    private int type;
    private String command;
    private String data;
    private int status;

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getCommand() {
        return command;
    }

    public void setCommand(String command) {
        this.command = command;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
}
